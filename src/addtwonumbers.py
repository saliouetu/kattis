def addtwonumbers(a, b):
    """
    Return the sum of two numbers.

    Parameters
    ----------
    a : int or float
        The first number.
    b : int or float
        The second number.

    Returns
    -------
    int or float
        The sum of `a` and `b`.

    Examples
    --------
    >>> addtwonumbers(2, 3)
    5
    >>> addtwonumbers(1.5, 2.5)
    4.0
    """
    return a + b
